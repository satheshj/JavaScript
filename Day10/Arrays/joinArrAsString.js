//join() - used to convert array to string
var product = ["Stationary","pen",15,"reynolds","Ball Point", "Gel"];
document.write(product.join()+"<br/>");
document.write(product.join(",")+"<br/>");
document.write(product.join(":")+"<br/>");
//toString() - used to convert any type of array elements into a string.
document.write(product.toString() + "<br/>")